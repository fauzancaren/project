    <script>
        const scrollContainers = document.querySelectorAll("#infiniteScroll--left");

        scrollContainers.forEach((containerCarousel) => {
            const scrollWidth = containerCarousel.scrollWidth;
            let isScrollingPaused = false;

            window.addEventListener("load", () => {
                self.setInterval(() => {
                    if (isScrollingPaused) {
                        return;
                    }
                    const first = containerCarousel.querySelector("article");

                    if (!isElementInViewport(first)) {
                        containerCarousel.appendChild(first);
                        containerCarousel.scrollTo(containerCarousel.scrollLeft - first.offsetWidth, 0);
                    }
                    if (containerCarousel.scrollLeft !== scrollWidth) {
                        containerCarousel.scrollTo(containerCarousel.scrollLeft + 1, 0);
                    }
                }, 15);
            });

            function isElementInViewport(el) {
                var rect = el.getBoundingClientRect();
                return rect.right > 0;
            }

            function pauseScrolling() {
                isScrollingPaused = true;
            }

            function resumeScrolling() {
                isScrollingPaused = false;
            }
            const allArticles = container.querySelectorAll("article");
            for (let article of allArticles) {
                article.addEventListener("mouseenter", pauseScrolling);
                article.addEventListener("mouseleave", resumeScrolling);
            }
        });
    </script>


